namespace App\Nova\Filters;

use Illuminate\Http\Request;
use Laravel\Nova\Filters\Filter;

class TwilioCallSourceFilter extends Filter
{
    const ENROLMENT_DASHBOARD = 'enrolment-dashboard';
    const PATIENT_CALL        = 'patient-call-page';

    /**
     * The filter's component.
     *
     * @var string
     */
    public $component = 'select-filter';

    /**
     * The displayable name of the filter.
     *
     * @var string
     */
    public $name = 'Source';

    /**
     * Apply the filter to the given query.
     *
     * @param  \Illuminate\Database\Eloquent\Builder $query
     * @param  mixed                                 $value
     * @return \Illuminate\Database\Eloquent\Builder
     */
    public function apply(Request $request, $query, $value)
    {
        return $query->where('source', '=', $value);
    }

    /**
     * Get the filter's available options.
     *
     * @return array
     */
    public function options(Request $request)
    {
        return [
            'Patient Call' => self::PATIENT_CALL,
            'Enrolment'    => self::ENROLMENT_DASHBOARD,
        ];
    }
}
