            try {
                if ($enrollee) {
                    $deleted = $enrollee->delete();
                }
        
                if ($user = $enrollee->user) {
                    if ($patientInfo = $user->patientInfo) {
                        $deleted = $patientInfo->delete();
                    }
                    $deleted = $user->delete();
