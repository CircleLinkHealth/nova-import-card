<template>
    <div class="container">
        <div class="row justify-content-center">
            <div class="col-md-12">
                <div class="card">
                    <div class="card-header">Send {{getSurveyName()}} Assessment Link for {{patientName}}</div>

                    <div class="card-body">

                        <div class="spinner-overlay" v-show="waiting">
                            <div class="text-center">
                                <font-awesome-icon icon="spinner" :spin="true"/>
                            </div>
                        </div>

                        <p>
                            Click below to send via {{getChannelName()}}.
                        </p>
                        <p>
                            <mdb-btn @click="inputChannelDetails()" :disabled="waiting || success">
                                Select Recipient
                            </mdb-btn>
                        </p>
                        <p>
                            <mdb-alert v-if="error" color="danger">
                                {{error}}
                            </mdb-alert>

                            <mdb-alert v-if="success" color="success">
                                Link sent. You can close this window.
                            </mdb-alert>
                        </p>
                    </div>
                </div>
            </div>
        </div>
        <send-link-modal v-if="sendLinkModalOptions.show" :options="sendLinkModalOptions" :only-email="onlyEmail()" :only-sms="onlySms()"></send-link-modal>
    </div>
</template>

<script>

    import {mdbAlert, mdbBtn} from 'mdbvue';
    import {library} from '@fortawesome/fontawesome-svg-core';
    import {faSpinner} from '@fortawesome/free-solid-svg-icons';
    import {FontAwesomeIcon} from '@fortawesome/vue-fontawesome';
    import SendLinkModal from './SendLinkModal';

    library.add(faSpinner);

    export default {
        name: "SendAssessmentLink",
        components: {mdbBtn, mdbAlert, FontAwesomeIcon, SendLinkModal},
        props: ['patientName', 'patientId', 'surveyName', 'channel'],
        data() {
            return {
                sendLinkModalOptions: {
                    debug: this.debug,
                    show: false,
                    survey: null,
                    patientId: null,
                    onDone: null,
                },
                waiting: false,
                success: false,
                error: null,
            }
        },
        methods: {
            inputChannelDetails() {
                this.sendLinkModalOptions.patientId = this.patientId;
                this.sendLinkModalOptions.survey = this.surveyName;
                this.sendLinkModalOptions.show = true;
                this.sendLinkModalOptions.onDone = () => {
                    this.sendLinkModalOptions.show = false;
                }
            },

            onlySms(){
              return this.channel == 'sms';
            },

            onlyEmail(){
                return this.channel == 'email';
            },

            getSurveyName(){
                if (this.surveyName == 'hra'){
                    return 'HRA';
                }else{
                    return 'Vitals';
                }
            },

            getChannelName(){
                if (this.channel == 'sms'){
                    return 'SMS';
                }{
                    return 'Email'
                }
            },

            handleError(error) {
                console.log(error);
                if (error.response && error.response.status === 504) {
                    this.error = "Server took too long to respond. Please try again.";
                }
                else if (error.response && error.response.status === 500) {
                    this.error = "There was an error with our servers. Please contact CLH support.";
                    console.error(error.response.data);
                }
                else if (error.response && error.response.status === 404) {
                    this.error = "Not Found [404]";
                }
                else if (error.response && error.response.status === 419) {
                    this.error = "Not Authenticated [419]";
                    //reload the page which will redirect to login
                    window.location.reload();
                }
                else if (error.response && error.response.data) {
                    const errors = [error.response.data.error];
                    Object.keys(error.response.data.errors || []).forEach(e => {
                        errors.push(error.response.data.errors[e]);
                });
                    this.error = errors.join('<br/>');
                } else {
                    this.error = error.message;
                }
            }
        },
        mounted() {

        },
        created() {

        }
    }
</script>

